package com.example.notify;

public class staff {
    String staffId, staffName;

    staff() {

    }

    public staff(String staffId, String staffName) {
        this.staffId = staffId;
        this.staffName = staffName;
    }

    public String getStaffId() {
        return staffId;
    }

    public String getStaffName() {
        return staffName;
    }
}
